public class Example5 {
    public static void main(String[] args) {

        String s1 = "HELLO";

        int len = s1.length();
        System.out.println(s1.charAt(0)); //H
        System.out.println(s1.charAt(1)); //E
        System.out.println(s1.charAt(len-1)); //O
    }


}
